package javaconceptprogramming;

public class DowhileLoop {

	public static void main(String[] args) {
		//Atleast one statement execute without checking any condition
		
		//1 to 10 number
		
		int a=1;
		
		do
		{
			System.out.println(a);
			a++;
		} while (a<=10); // Semicolon is require in do while loop

	}

}
