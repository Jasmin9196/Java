package javaconceptprogramming;

public class DataTypesDemo {

	public static void main(String[] args) {
		//Numeric data type
		int a=100, b=200;
		System.out.println("the value of a is:" +a);
		System.out.println("the value of b is:" +b);
		System.out.println(a+b);
		
		System.out.println("The sum of a and b is:"+(a+b));
		
		byte by=125;
		System.out.println(by);
		
		short sh=32578;
		System.out.println(sh);
		
		long l=12456897888l; //literal is needed l/L
		System.out.println(l); 
		
		//Decimal numbers- float and double
		
		float f=1.8578967F; //literal is needed f/F
		System.out.println(f);
		
		double d=1.56897845968978;
		System.out.println(d);
		
		char grade='A';
		System.out.println(grade);
		
		String name= "jasmin";
		System.out.println(name);
		
		boolean bl=false; //allow only true and false //Size 1 bit
		System.out.println(bl);
		
		

		
		
		
		
		
	}

}
